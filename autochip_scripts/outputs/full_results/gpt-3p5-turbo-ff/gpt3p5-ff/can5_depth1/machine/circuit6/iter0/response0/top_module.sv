module top_module (
   input [2:0] a, 
   output reg [15:0] q
);
   always @*
   begin
      case (a)
         3'b000: q <= 16'b0100100011101010;
         3'b001: q <= 16'b1010111010010000;
         3'b010: q <= 16'b0010100110000100;
         3'b011: q <= 16'b0101101110110110;
         3'b100: q <= 16'b0010000101001010;
         3'b101: q <= 16'b0110011110010110;
         3'b110: q <= 16'b1100011010001110;
         3'b111: q <= 16'b0010110111101001;
         default: q <= 16'b0000000000000000;
      endcase
   end
endmodule
