module top_module (
	input [2:0] a,
	output reg [15:0] q
);

	always @(*)
	begin
		case(a)
			3'b000: q = 16'b0001001001100010;
			3'b001: q = 16'b1010111001100000;
			3'b010: q = 16'b0010100110010100;
			3'b011: q = 16'b0101101111101110;
			3'b100: q = 16'b0010000100101010;
			3'b101: q = 16'b0110011101010110;
			3'b110: q = 16'b1100011011010110;
			3'b111: q = 16'b0010110111011001;
		endcase
	end
endmodule
