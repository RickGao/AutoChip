module top_module (
    input [4:1] x,
    output logic f
);

always @(*) begin
    f = ( (~x[3] & ~x[4] & ~x[2] & ~x[1]) |  // 0000
          (~x[3] & ~x[4] &  x[2] & ~x[1]) |  // 0010
          (~x[3] &  x[4] & ~x[2] & ~x[1]) |  // 0100
          (~x[3] &  x[4] &  x[2] & ~x[1]) |  // 0110
          ( x[3] & ~x[4] & ~x[2] & ~x[1]) |  // 1000
          ( x[3] & ~x[4] &  x[2] & ~x[1]) |  // 1010
          ( x[3] & ~x[4] &  x[2] &  x[1]) |  // 1011
          ( x[3] &  x[4] & ~x[2] & ~x[1]) |  // 1100
          ( x[3] &  x[4] & ~x[2] &  x[1]) |  // 1101
          ( x[3] &  x[4] &  x[2] &  x[1])    // 1111
        );
end

endmodule
