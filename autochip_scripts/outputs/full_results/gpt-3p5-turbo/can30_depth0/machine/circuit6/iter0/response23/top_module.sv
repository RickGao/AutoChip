module top_module (
	input [2:0] a, 
	output reg [15:0] q
);

	always @(*) begin
		case(a)
			3'b000: q = 16'b0100100110111010;
			3'b001: q = 16'b1010111001110000;
			3'b010: q = 16'b0010011110111100;
			3'b011: q = 16'b0111001100110110;
			3'b100: q = 16'b0000101001111010;
			3'b101: q = 16'b0110011110101110;
			3'b110: q = 16'b1100001110000110;
			3'b111: q = 16'b0010110110111001;
		endcase
	end

endmodule
