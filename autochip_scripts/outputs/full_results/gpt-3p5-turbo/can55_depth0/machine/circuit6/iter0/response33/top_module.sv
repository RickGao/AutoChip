			3'b011: q = 16'b0101101110011110;
			3'b100: q = 16'b0010000001001010;
			3'b101: q = 16'b0110010111001110;
			3'b110: q = 16'b1100000000110110;
			3'b111: q = 16'b0010110100011001;
		endcase
	end

endmodule
