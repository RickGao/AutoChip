module top_module (
	input [2:0] a,
	output reg [15:0] q
);

	always @(a) begin
		case(a)
			3'b000: q <= 16'b0100100101110010;
			3'b001: q <= 16'b1010111001110000;
			3'b010: q <= 16'b0010100110100100;
			3'b011: q <= 16'b0101101111111110;
			3'b100: q <= 16'b0000000100001010;
			3'b101: q <= 16'b0110001011110110;
			3'b110: q <= 16'b1100001111001110;
			3'b111: q <= 16'b0010110100111001;
		endcase
	end

endmodule
