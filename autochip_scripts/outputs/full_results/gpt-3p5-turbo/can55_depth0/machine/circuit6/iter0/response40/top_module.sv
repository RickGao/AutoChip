module top_module (
	input [2:0] a, 
	output reg [15:0] q
);

	always @(*)
	begin
		case (a)
			3'b000: q = 16'b0100100100101010;
			3'b001: q = 16'b1010110110100000;
			3'b010: q = 16'b0010011000001100;
			3'b011: q = 16'b0101101110001110;
			3'b100: q = 16'b0010000011110010;
			3'b101: q = 16'b0110010111001110;
			3'b110: q = 16'b1100001110100110;
			3'b111: q = 16'b0010111011001001;
		endcase
	end

endmodule
