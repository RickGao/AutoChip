module top_module (
	input [2:0] a, 
	output reg [15:0] q
);

	always @* begin
		case(a)
			3'b000: q = 16'b0100100101110010;
			3'b001: q = 16'b101011110100000;
			3'b010: q = 16'b0010011111110100;
			3'b011: q = 16'b0101101001110110;
			3'b100: q = 16'b0010000010111010;
			3'b101: q = 16'b0110010111001110;
			3'b110: q = 16'b1100011000111110;
			3'b111: q = 16'b0010111010001001;
			default: q = 16'b0;
		endcase
	end

endmodule
