module top_module (
	input [2:0] a, 
	output reg [15:0] q
);
	always @(a) begin
		case (a)
			0: q <= 16'b0100100000111010;
			1: q <= 16'b1010111101000000;
			2: q <= 16'b0010100000010100;
			3: q <= 16'b0101100111111110;
			4: q <= 16'b0010000010101010;
			5: q <= 16'b0111111111100110;
			6: q <= 16'b1100001100111110;
			7: q <= 16'b0010110110111001;
			default: q <= 16'b0;
		endcase
	end
endmodule
