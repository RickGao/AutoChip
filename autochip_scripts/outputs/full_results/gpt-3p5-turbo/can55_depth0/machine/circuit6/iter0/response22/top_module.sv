module top_module (
    input [2:0] a,
    output reg [15:0] q
);

    always @*
    begin
        case(a)
            0: q = 16'b0000010011010010;
            1: q = 16'b1010111001000000;
            2: q = 16'b0010110011100100;
            3: q = 16'b0101101000110110;
            4: q = 16'b0000100010011010;
            5: q = 16'b0110011111001110;
            6: q = 16'b1100001110111110;
            7: q = 16'b0010111001101001;
        endcase
    end

endmodule
