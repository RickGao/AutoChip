module top_module (
	input [2:0] a, 
	output reg [15:0] q
);

	always @(*) begin
		case (a)
			3'b000: q = 16'b0001001001101010;
			3'b001: q = 16'b1010110011000000;
			3'b010: q = 16'b0010011001110100;
			3'b011: q = 16'b0101101011101110;
			3'b100: q = 16'b0010000010111010;
			3'b101: q = 16'b0110010010101110;
			3'b110: q = 16'b1100001111001110;
			3'b111: q = 16'b0010111011111001;
		endcase
	end
endmodule
