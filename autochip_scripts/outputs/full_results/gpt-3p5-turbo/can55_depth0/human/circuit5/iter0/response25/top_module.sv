            {4'b0000, 4'b0001}: q = 4'b1110;  // 1
            {4'b0000, 4'b0010}: q = 4'b0001;  // 2
            {4'b0000, 4'b0011}: q = 4'b1101;  // 3
            {4'b0000, 4'b0100}: q = 4'b1111;  // 4
            {4'b0000, 4'b0101}: q = 4'b1111;  // 5
            {4'b0000, 4'b0110}: q = 4'b1111;  // 6
            {4'b0000, 4'b0111}: q = 4'b1111;  // 7
            {4'b0000, 4'b1000}: q = 4'b1111;  // 8
            {4'b0000, 4'b1001}: q = 4'b1111;  // 9
            {4'b0000, 4'b1010}: q = 4'b1111;  // a
            {4'b0000, 4'b1011}: q = 4'b1111;  // b
            {4'b0000, 4'b1100}: q = 4'b1111;  // c
            {4'b0000, 4'b1101}: q = 4'b1111;  // d
            {4'b0000, 4'b1110}: q = 4'b1111;  // e
            {4'b0000, 4'b1111}: q = 4'b1111;  // f
            default: q = 4'b0000;
        endcase
    end
endmodule
