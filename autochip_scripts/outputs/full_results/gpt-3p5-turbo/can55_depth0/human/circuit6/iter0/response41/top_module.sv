module top_module (
	input [2:0] a, 
	output reg [15:0] q
);

always @* begin
	case(a)
		3'b000: q = 16'b0000000001001100;
		3'b001: q = 16'b1010111011100000;
		3'b010: q = 16'b0010011111010100;
		3'b011: q = 16'b0100000011010110;
		3'b100: q = 16'b0110010011101110;
		3'b101: q = 16'b1100010100101110;
		3'b110: q = 16'b1100010100101110;
		3'b111: q = 16'b0010100100001110;
	endcase
end

endmodule
