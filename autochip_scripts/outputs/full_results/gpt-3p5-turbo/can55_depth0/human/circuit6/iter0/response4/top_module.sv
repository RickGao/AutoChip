module top_module (
	input [2:0] a, 
	output reg [15:0] q
);

    always @(a) begin
        case (a)
            3'b000: q = 16'b0000000000000000;
            3'b001: q = 16'b0001001011110000;
            3'b010: q = 16'b0010011111010100;
            3'b011: q = 16'b0011010100011110;
            3'b100: q = 16'b0100000011000110;
            3'b101: q = 16'b0101001101001110;
            3'b110: q = 16'b0110010101010010;
            3'b111: q = 16'b0111011100001001;
            default: q = 16'b0000000000000000;
        endcase
    end
    
endmodule
