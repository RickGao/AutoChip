module top_module (
    input [2:0] a,
    output reg [15:0] q
);
    always @(a) begin
        case (a)
            0: q = 16'b0000000000110010;
            1: q = 16'b1010111011100000;
            2: q = 16'b0010011111010100;
            3: q = 16'b0101101000001110;
            4: q = 16'b0010000001100110;
            5: q = 16'b0110010011001110;
            6: q = 16'b1100010100010010;
            7: q = 16'b0010111100011001;
            default: q = 16'b0000000000000000;
        endcase
    end
endmodule
