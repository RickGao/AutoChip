module top_module (
	input [2:0] a, 
	output reg [15:0] q
);

	always @*
	begin
		case(a)
			3'b000: q = 16'b0100100011011010;
			3'b001: q = 16'b1010111010100000;
			3'b010: q = 16'b0010010010010100;
			3'b011: q = 16'b1011001100100110;
			3'b100: q = 16'b0010000000111010;
			3'b101: q = 16'b0110010111010110;
			3'b110: q = 16'b1100001110110110;
			3'b111: q = 16'b0010110111101001;
			default: q = 16'b0000000000000000;
		endcase
	end
	
endmodule
