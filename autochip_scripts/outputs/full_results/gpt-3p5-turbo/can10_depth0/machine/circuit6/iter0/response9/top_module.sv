module top_module (
	input [2:0] a, 
	output reg [15:0] q
);

	always @(*) begin
		case(a)
			3'b000: q = 16'b0100100010110010;
			3'b001: q = 16'b1010110100100000;
			3'b010: q = 16'b0010011110010100;
			3'b011: q = 16'b0101101001011110;
			3'b100: q = 16'b0010000100001010;
			3'b101: q = 16'b0110010011111110;
			3'b110: q = 16'b1100001111101110;
			3'b111: q = 16'b0010110111101001;
			default: q = 16'b0;
		endcase
	end

endmodule
