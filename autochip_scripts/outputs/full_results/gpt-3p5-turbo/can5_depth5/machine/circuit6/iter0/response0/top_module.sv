module top_module (
	input [2:0] a,
	output reg [15:0] q
);

	always @(*) begin
		case (a)
			3'b000: q = 16'b0100100100011010;
			3'b001: q = 16'b1010111000100000;
			3'b010: q = 16'b0010011011010100;
			3'b011: q = 16'b0101101110110110;
			3'b100: q = 16'b0000001001101010;
			3'b101: q = 16'b0110011000100110;
			3'b110: q = 16'b1100001111100110;
			3'b111: q = 16'b0010111101111001;
			default: q = 16'b0;
		endcase
	end
	
endmodule
