module top_module(
    input [3:1] y,
    input w,
    output reg Y2);

always @(y or w)
    case({y, w})
        // A (0) --0--> B
        // A (0) --1--> A
        3'b0000: Y2 = 1'b1;
        3'b0001: Y2 = 1'b0;
        // B (0) --0--> C
        // B (0) --1--> D
        3'b0010: Y2 = 1'b1;
        3'b0011: Y2 = 1'b0;
        // C (0) --0--> E
        // C (0) --1--> D
        3'b0100: Y2 = 1'b1;
        3'b0101: Y2 = 1'b0;
        // D (0) --0--> F
        // D (0) --1--> A
        3'b0110: Y2 = 1'b1;
        3'b0111: Y2 = 1'b0;
        // E (1) --0--> E
        // E (1) --1--> D
        3'b1000: Y2 = 1'b1;
        3'b1001: Y2 = 1'b0;
        // F (1) --0--> C
        // F (1) --1--> D
        3'b1010: Y2 = 1'b1;
        3'b1011: Y2 = 1'b0;
    endcase

endmodule
