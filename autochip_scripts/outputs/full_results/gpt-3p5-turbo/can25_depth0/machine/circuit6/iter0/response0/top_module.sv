module top_module (
    input [2:0] a,
    output reg [15:0] q
);

    always @(*) begin
        case (a)
            3'b000: q = 16'b0100100011101010;
            3'b001: q = 16'b1010111101000000;
            3'b010: q = 16'b0010010110010100;
            3'b011: q = 16'b0101101100110110;
            3'b100: q = 16'b0010000110010010;
            3'b101: q = 16'b0110010100001110;
            3'b110: q = 16'b1100011000100110;
            3'b111: q = 16'b0010111000011001;
            default: q = 16'b0;
        endcase
    end

endmodule
