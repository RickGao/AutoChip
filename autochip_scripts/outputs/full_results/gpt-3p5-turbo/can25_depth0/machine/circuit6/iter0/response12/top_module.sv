module top_module (
	input [2:0] a,
	output reg [15:0] q
);

	always @* begin
		case (a)
			3'b000: q = 16'b0100100011010010;
			3'b001: q = 16'b1010111011010000;
			3'b010: q = 16'b0010011110110100;
			3'b011: q = 16'b0101101111100110;
			3'b100: q = 16'b0010000010011010;
			3'b101: q = 16'b0110001100110110;
			3'b110: q = 16'b1100001101011110;
            3'b111: q = 16'b0010110110011001;
            default: q = 16'b0;
		endcase
	end

endmodule
